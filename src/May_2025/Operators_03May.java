package May_2025;

public class Operators_03May {
    public static void main(String[] args) {
        // Arithmetic Operator
// +,-,/,*
        int a = 10;
        int b = 2;
        System.out.println(a+b); // + -> binary - 2 operands
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}
